package com.coffeex.dto;

public class PromoteDto {

	String position;
	String promotestaffid;
	String pdate;
	
	public PromoteDto() {
		
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public String getPromotestaffid() {
		return promotestaffid;
	}

	public void setPromotestaffid(String promotestaffid) {
		this.promotestaffid = promotestaffid;
	}

	public String getPdate() {
		return pdate;
	}

	public void setPdate(String pdate) {
		this.pdate = pdate;
	}

}
